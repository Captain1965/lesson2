#Создадим массив чисел номер элемента первый элемент массива 0 , последующие соотвествуют числу дней в 
#в месяце совпадающем номером элемента массива.
#сформируем новый массив из элементов от нулевого до месяца предшествующего введенному пользователем
#вычислим сумму элементов массива плюс количество дней в соотвествии с введеной пользователем датой
